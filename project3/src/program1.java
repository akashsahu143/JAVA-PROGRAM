
import java.util.*;
public class program1 {
    public static void main(String[] args) {
        int counter = 0;


        while (counter <= 10) {
            System.out.println(counter);
            counter++;
        }


        counter = 10;
        while (counter >= 0) {
            System.out.println(counter);
            counter--;
        }
    }
}
